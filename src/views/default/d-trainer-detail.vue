<template>
  <main style="margin-top: 125px">
    <div class="section1800">
      <div
        v-if="Object.keys(trainerInfo).length"
        class="card mx-auto card-container"
        style="width: 18rem"
      >
        <!-- 카드 이미지 -->
        <img
          :src="getImagePath(trainerInfo.mainimage)"
          class="card-img-top"
          alt="Trainer Image"
        />
        <!-- 카드 본문 -->
        <div class="card-body">
          <h5 class="card-title">{{ trainerInfo.memberVO.name }}</h5>
          <p class="card-text">{{ trainerInfo.trainerintro }}</p>
        </div>
        <!-- 신청 및 기능 -->
        <div class="card-body">
          <btn href="#" class="card-btn">상세보기 혹은 다른 기능</btn>
          <button class="card-btn" @click="applyPT">PT 신청 🏋️‍♀️</button>
        </div>
        <!-- 카드 리스트 -->
        <ul class="list-group list-group-flush">
          <li class="list-group-item">{{ trainerInfo.location }}</li>
          <!-- 상에 대한 정보를 표시하고 싶다면 이렇게 할 수 있습니다. -->
          <li class="list-group-item">{{ trainerInfo.awards1 }}</li>
          <li class="list-group-item">{{ trainerInfo.awards2 }}</li>
          <!-- 기타 수상 내역도 추가할 수 있습니다. -->
        </ul>
      </div>
      <div class="detail-description">
      <!-- 여기에 상세 설명 내용을 넣습니다. -->
      <h2>트레이너 상세 정보</h2>
      <p>여기에 트레이너에 대한 추가적인 설명이나 정보를 넣을 수 있습니다.</p>
      <!-- 더 많은 내용 추가 가능 -->
    </div>
    </div>
  </main>
</template>

<script>
export default {
  data() {
    return {
      trainerInfo: {}, // 데이터 객체로 받기
      trainerId: "",
    };
  },
  methods: {
    // 파일명 인코딩용 스크립트
    getImagePath(fileName) {
      const basePath = "http://localhost/springpt/images/trainer/";
      const encodedFileName = encodeURIComponent(fileName);
      return `${basePath}${encodedFileName}`;
    },
    // 트레이너 디테일 가져오기
    async fetchTrainerDetail() {
      try {
        // id 바인딩
        this.trainerId = this.$route.params.id;
        console.log("여기입니다 여러분", this.trainerId);
        // 가져온 ID를 사용하여 상세 정보를 요청
        const response = await this.$axios.get(
          `/trainerList/${this.trainerId}`
        );
        this.trainerInfo = response.data;
        console.log("리스폰스", response);
        console.log(this.trainerInfo);
      } catch (e) {
        console.log("여기가 에러", e);
      }
    },
    // .card-btn 누를 시 | PT신청 메서드_이성한
    async applyPT() {
      try {
        // PT 신청 데이터 준비
        console.log(this.trainerId);
        const requestData = {
          userId: "", // token인증으로 대체
          trainerId: this.$route.params.id, // 트레이너 이름 넣기
          roomId: "",
          status: "wait",
        };

        // PT 신청 API 요청
        const response = await this.$axios.post("/matching", requestData);

        // 성공적인 요청 처리
        console.log("전송 성공:", response.data);
        this.$swal.fire({
          icon: "success",
          title: "PT상담이 신청되었습니다!",
          text: "연락을 기다려주시기 바랍니다.",
        });
      } catch (e) {
        this.$swal
        .fire({
            icon: "warning",
            title: "PT상담이 이미 신청 되어 있습니다!",
            text: "기존 PT상담을 취소 하시겠습니까?",
            showCancelButton: true,
            confirmButtonColor: "#d33",
            cancelButtonColor: "#3085d6",
            confirmButtonText: "기존 PT예약 삭제",
            cancelButtonText: "아니요, 유지하겠습니다.",
          })
          .then((result) => {
            if (result.value) {
              const requestData = {
                userId: "", // token인증으로 대체
                trainerId: this.$route.params.id, // 트레이너 이름 넣기
                roomId: "",
                status: "wait",
              };
              try {
                console.log("시도중");
                this.$axios.post("/matchCancle", requestData);
              } catch (e) {
                console.log("실패");
                console.log(e);
                this.$swal.fire({
                  icon: "warning",
                  title: "PT신청중에 있습니다!",
                  text: "PT선생님 변경은 기존 선생님과 상의 후 진행하시기 바랍니다.",
                  
                });
              }
              const Toast = this.$swal.mixin({
                toast: true,
                position: "center-center",
                showConfirmButton: false,
                timer: 8000,
                timerProgressBar: true,
                didOpen: (toast) => {
                  toast.addEventListener("mouseenter", this.$swal.stopTimer);
                  toast.addEventListener("mouseleave", this.$swal.resumeTimer);
                },
              });

              Toast.fire({
                icon: "success",
                title: "이전 PT상담이 취소 되었습니다.",
                text: "다시 PT 예약해주시기 바랍니다.",
              });
            }
          });

        console.error("전송 실패:", e);
      }
    },
  },
  mounted() {
    this.fetchTrainerDetail();
  },
};
</script>



